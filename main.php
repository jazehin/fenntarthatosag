<p>A témafeldolgozások formája, módszertana szabadon választható. A diákokkal közös gondolkodás oldotta a
    feszültségeket, a megoldási alternatívák feltárása pozitívabb jövőképet sugallt. Tanítványaink ismeretanyagának
    bővítésével érdemben járultunk hozzá a következő nemzedék fenntartható fejlődésről kialakított elképzelései
    formálásához, használható gyakorlatok megismeréséhez.</p>
<p>
    Technikumunk belső hálózatának „K” kaptárában lévő „Fenntarthatóság 2022-2023” mappában folyamatosan
    dokumentálható
    a tevékenységünk. Tehetségígéreteink önállóan, vagy kis csoportban létrehozott szellemi termékeit a
    Fenntarthatósági
    Témahéten megrendezett tehetségnapon, 2023. április 26-án kívánjuk bemutatni.
    A tehetséges, 14.SZ osztályba járó diákjaink, Horváth Attila kollégánk irányításával, olyan weblapot készítenek,
    amelyre feltölthetjük, így közkinccsé tehetjük a tanév során elkészült valamennyi projektünk eredményeit.
</p>