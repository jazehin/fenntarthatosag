<h1 class="fs-2 text-center">Tóthné Engler Zsuzsanna: A háború</h1>

<p>A fenntarthatósági témahét program-sorozatra készülve, a rendészet ágazat a HÁBORÚ témáját dolgozta fel. A
    feladat megvalósítása: A két 11-es osztályban (11.P és 11.R), párhuzamosan, versenyeztetve a két osztályt,
    fogtunk neki a feladat megvalósításának. Azért erre az évfolyamra esett a választás, mert ők már szakmai
    tárgyakból elég tudással (pl. jog) rendelkeznek a téma feldolgozásához, másrészt nem állnak semmilyen nagyobb
    megmérettetés előtt (ágazati vizsga, mellék-szakképesítési vizsga, érettségi vizsga). A rendészeti ágazatos
    oktatók közül magamra vállaltam ezt a feladatot és én mentoráltam végig a tanulókat a feladatok kiosztásától és
    átbeszélésétől kezdve, egészen a digitális megjelenítésig.</p>

